const digitA = prompt("Masukkan Angka pertama");
const digitB = prompt("Masukkan angka kedua");

const resultAddition = digitA + digitB;
const resultSubstraction = digitA - digitB;
const resultMultiplication = digitA * digitB;
const resultDivision = digitA / digitB;

console.log(`Angka ${digitA} + ${digitB} adalah ${resultAddition}`);
console.log(`Angka ${digitA} - ${digitB} adalah ${resultSubstraction}`);
console.log(`Angka ${digitA} x ${digitB} adalah ${resultMultiplication}`);
console.log(`Angka ${digitA} : ${digitB} adalah ${resultDivision}`);
